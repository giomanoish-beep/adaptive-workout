#!/usr/bin/env node

/**
 * Bundles Supabase Edge Functions for deployment.
 *
 * The generate-workout function imports internal workspace packages via
 * relative paths (e.g. ../../../packages/...). The Supabase CLI and Deno
 * runtime cannot resolve monorepo imports outside the function directory.
 *
 * This script uses esbuild to produce a single self-contained index.ts per
 * function that the Supabase CLI can deploy directly.
 *
 * Usage: node scripts/build-edge-function.mjs <function-name>
 * e.g.:  node scripts/build-edge-function.mjs generate-workout
 */

import * as esbuild from 'esbuild';
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const functionName = process.argv[2];
if (!functionName) {
  console.error('Usage: node scripts/build-edge-function.mjs <function-name>');
  process.exit(1);
}

const fnDir = resolve(import.meta.dirname, '..', 'supabase', 'functions', functionName);
const entryPoint = resolve(fnDir, 'index.ts');
const outFile = resolve(fnDir, 'index.bundle.ts');

console.log(`📦 Bundling edge function: ${functionName}`);
console.log(`   Entry: ${entryPoint}`);
console.log(`   Output: ${outFile}`);

try {
  await esbuild.build({
    entryPoints: [entryPoint],
    bundle: true,
    outfile: outFile,
    format: 'esm',
    platform: 'neutral',
    target: 'esnext',
    sourcemap: false,
    minify: false,
    treeShaking: true,
    external: ['https://deno.land/*', 'https://esm.sh/*', 'https://npm.deno.dev/*'],
    banner: {
      js: '// Generated by scripts/build-edge-function.mjs — DO NOT EDIT\n// This file is auto-generated for Supabase Edge Function deployment.\n',
    },
    resolveExtensions: ['.ts', '.tsx', '.js', '.jsx', '.mjs'],
  });

  // Read back the output to inject the deno-specific import markers
  const bundled = readFileSync(outFile, 'utf-8');

  // esbuild may leave .ts extensions in import paths; Deno requires them
  // but esbuild already strips them in bundled output, so this should be fine.

  writeFileSync(outFile, bundled, 'utf-8');
  console.log(`✅ Bundled ${functionName} -> ${outFile}`);
  console.log(`   Size: ${(Buffer.byteLength(bundled) / 1024).toFixed(1)} KB`);
} catch (err) {
  console.error(`❌ Failed to bundle ${functionName}:`, err.message);
  process.exit(1);
}
